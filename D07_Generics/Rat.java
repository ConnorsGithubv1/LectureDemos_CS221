public class Rat implements Animal {
    public void eat() {
        System.out.println("I eat almost anything!");
    }

    public void speak() {
        System.out.println("Squeak squeak squeak!");
    }

    public void explore() {
        System.out.println("I like to get into everything!");
    }

    public String toString() {
        return "I am a rat.";
    }
}
